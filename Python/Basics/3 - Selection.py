# all programming lanuages include selector (if statments)
def main(num):
    if num < 5:
        print("num is less then 5")

# if else statement
def main2(num):
    if num < 5 :
        print("num is less then 5")
    else:
        print("num is greater then 5")

# if elif statment
def main3(num):
    if num == 5 :
        print("num is 5")
    elif num < 5:
        print("num is less then 5")
    else:
        print("num is greater then 5")

if __name__ == "__main__":
    main(2)
